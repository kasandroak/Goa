# Task 1: String Formatting
template = "Hello, {name}. Welcome to {place}."
formatted_string = template.format(name="Alice", place="Wonderland")
print("Formatted String:", formatted_string)

# Task 2: Joining Strings
words = ["apple", "banana", "cherry"]
fruit_string = ", ".join(words)
print("Fruit String:", fruit_string)

# Task 3: Splitting a String
sentence = "The quick brown fox jumps over the lazy dog."
words_list = sentence.split()
print("Words List:", words_list)

# Task 4: Replacing Substrings
quote = "To be or not to be, that is the question."
modified_quote = quote.replace("be", "code")
print("Modified Quote:", modified_quote)

# Task 5: Converting to Lowercase
mixed_case = "PyThOn Is AwEsOmE!"
lowercase_string = mixed_case.lower()
print("Lowercase String:", lowercase_string)

# Task 6: Converting to Uppercase
greeting = "good morning"
uppercase_greeting = greeting.upper()
print("Uppercase Greeting:", uppercase_greeting)

