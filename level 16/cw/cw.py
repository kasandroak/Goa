# 5) Examples of comparison operations
print("5) Comparison operations:")
print(5 > 3)   # True
print(10 < 20) # True
print(15 == 15) # True
print(8 != 7)  # True
print(4 >= 2)  # True
print(10 <= 10) # True
print(3 > 7)   # False
print(9 == 5)  # False
print(2 < 1)   # False
print(6 >= 8)  # False

# 6) Examples of logical operations
print("\n6) Logical operations:")
print(True and True)   # True
print(True and False)  # False
print(False and False) # False
print(True or False)   # True
print(False or False)  # False
print(not True)        # False
print(not False)       # True
print(True and (5 > 3))  # True
print(False or (10 < 5)) # False
print(not (4 == 4))    # False

# 7) Examples of combinations of comparison and logical operations
print("\n7) Combined comparison and logical operations:")
print(5 < 10 and 10 > 5)     # True
print(3 > 2 or 2 < 1)        # True
print(not (7 == 7))          # False
print(8 <= 9 and 4 != 4)     # False
print(1 == 1 or 2 == 3)      # True
print(10 > 5 and not (6 < 4)) # True
print(4 <= 5 or 7 > 8)       # True
print(not (5 > 10) and (3 == 3)) # True
print(10 == 10 and 20 != 15) # True
print(not (9 < 2) or 3 > 1)  # True

