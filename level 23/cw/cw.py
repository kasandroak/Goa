# 1. სიის შექმნა, 5 მნიშვნელობის დამატება და 3-ის შეცვლა
my_list = ["apple", "banana", "cherry", "date", "elderberry"]
my_list[1] = "blueberry"  # banana შეცვლა
my_list[3] = "dragonfruit"  # date შეცვლა
my_list[4] = "fig"  # elderberry შეცვლა

print("შეცვლილი სია:", my_list)

# 2. რიცხვების სიის შექმნა და ჯამი
numbers = [1, 2, 3, 4, 5]
total_sum = sum(numbers)

print("რიცხვების ჯამი:", total_sum)

# 3. სტრინგების სიის შექმნა და ფრაზის შექმნა
words = ["Hello", "world", "this", "is", "a", "test"]
phrase = " ".join(words)

print("შექმნილი ფრაზა:", phrase)
