# დიდი ტექსტი, რომლის ნაწილები უნდა გავზრდოთ
large_paragraph = """საქართველო მდებარეობს აღმოსავლეთ ევროპასა და სამხრეთ კავკასიის რეგიონში, იგი საზღვარზეა ევროპასა და აზიას შორის. ქვეყანა ცნობილია თავისი უნიკალური კულტურით, ტრადიციებით და ღვინოსთან დაკავშირებული ისტორიით. საქართველოს მდიდარი ბუნება, სადაც სხვადასხვა ტიპის ეკოსისტემები და უნიკალური სახეობები აქვთ, დიდ ყურადღებას იპყრობს ტურისტებისგან. თბილისი, დედაქალაქი, თავისი ისტორიული ადგილებით და თანამედროვე არქიტექტურით ტურისტებს ხშირად იზიდავს."""

# მნიშვნელოვანი სიტყვები, რომლებიც უნდა გამოვყოთ
important_words = ["საქართველო", "ბუნება", "კულტურა", "ტრადიციები", "თბილი", "ღვინო", "ტურისტები"]

# ფუნქცია, რომელიც თითოეულ მნიშვნელოვან სიტყვას <strong> ტეგებით გაუსვამს ხაზს
def highlight_text(text, words):
    for word in words:
        text = text.replace(word, f"<strong>{word}</strong>")
    return text

# ტექსტის ხაზგასმა
highlighted_paragraph = highlight_text(large_paragraph, important_words)

# გამოტანა HTML ფორმატში
print("\n\nHighlighted Text:\n")
print(highlighted_paragraph)
