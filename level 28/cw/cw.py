# Example string
text = "hello world"

# Applying all the string functions
upper_text = text.upper()       # Converts to uppercase
lower_text = text.lower()       # Converts to lowercase
capitalize_text = text.capitalize()  # Capitalizes the first letter
find_text = text.find("world")  # Finds the index of 'world'
swapcase_text = text.swapcase()  # Swaps the case

# Printing all results
print("upper():", upper_text)
print("lower():", lower_text)
print("capitalize():", capitalize_text)
print("find():", find_text)
print("swapcase():", swapcase_text)
